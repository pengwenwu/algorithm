#include <cstdio>
const int maxn = 11;
//输出1~n的全排列
int n, P[maxn], hashTable[maxn] = {false};

void generateP(int index){
	if(index == n+1){
		for(int i=1;i<=n;i++){
			printf("%d", P[i]);
		}
		printf("\n");
		return;
	}
	for(int x = 1;x<=n;x++){
		if(hashTable[x]==false){
			P[index] = x;
			hashTable[x] = true;
			generateP(index+1);
			hashTable[x] = false; //递归处理完之后，将当前x释放 
		} 
	}
}

int main(){
		printf("请输入一个不大于11不小于1的数\n");
		scanf("%d",&n);
		//n =3;
		generateP(1);
		getchar();
		return 0;
} 

